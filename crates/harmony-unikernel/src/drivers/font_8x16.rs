#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn font_size_correct() {
        assert_eq!(FONT_8X16.len(), 4096);
    }

    #[test]
    fn space_is_blank() {
        let space = &FONT_8X16[0x20 * 16..(0x20 + 1) * 16];
        assert!(
            space.iter().all(|&b| b == 0),
            "space glyph should be all zeros"
        );
    }

    #[test]
    fn capital_a_has_content() {
        let a = &FONT_8X16[0x41 * 16..(0x41 + 1) * 16];
        assert!(
            a.iter().any(|&b| b != 0),
            "'A' glyph should have some set pixels"
        );
    }
}
